    private void payActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payActionPerformed
        // TODO add your handling code here:

    double total = myOrder.getTotal();
    if (total == 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: No items to pay.");
        return;
    }

    //  Ask user for Cash Input
    String input = javax.swing.JOptionPane.showInputDialog(this, "Total is " + total + "\nEnter Cash Amount:");

    //  to prevent crashing if the user click cancel
    if (input == null || input.isEmpty()) {
        return;
    }

    try {
        double cash = Double.parseDouble(input);

        if (cash >= total) { // to check if cash is enough
            double change = cash - total;
            
            String receipt = "--- ORDER RECEIPT ---\n" + 
                             Receipt.getText() +
                             "\n------------------------" +
                             "\nTotal:  " + total +
                             "\nCash:   " + cash + 
                             "\nChange: " + change;
            
            javax.swing.JOptionPane.showMessageDialog(this, receipt, "Transaction Complete", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            
            // Clear everything for the next customer (reuse the clear logic)
            redo.doClick(); 
            
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Insufficient Cash!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }

    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid number.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_payActionPerformed

