    private void availableTable() {
        DefaultTableModel model = (DefaultTableModel) BeverageMenu.getModel();
        model.setRowCount(0); // Clear existing rows

        // loops the existing menu.getSize() and .getItem() methods
        for (int i = 0; i < menu.getSize(); i++) {
            MenuOfBeverages item = menu.getItem(i);

            model.addRow(new Object[]{
                item.getName(),
                item.getCategory(),
                item.getPrice()
            });
        }
    }

