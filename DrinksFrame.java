    private void addOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addOrderActionPerformed
        // TODO add your handling code here:
        int selectedRow = BeverageMenu.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a drink from the table!");
            return;
        }

        int quantities = (int) quantity.getValue();

        MenuOfBeverages selectedDrink = menu.getItem(selectedRow); 

        ItemOrder newItem = new ItemOrder(selectedDrink, quantities); // reuse the existing class

        myOrder.addItem(newItem);

        Receipt.append(newItem.itemDetails() + "\n");

        lblTotal.setText("Total: Php " + myOrder.getTotal());

        quantity.setValue(1); // reset spinner
    }//GEN-LAST:event_addOrderActionPerformed

